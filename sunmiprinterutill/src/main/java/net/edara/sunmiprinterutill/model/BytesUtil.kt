    fun initTable(h: Int, w: Int): ByteArray {
        val hh = h * 32
        val ww = w * 4
        val data = ByteArray(hh * ww + 5)
        data[0] = ww.toByte() //xL
        data[1] = (ww shr 8).toByte() //xH
        data[2] = hh.toByte()
        data[3] = (hh shr 8).toByte()
        var k = 4
        var m = 31
        for (i in 0 until h) {
            for (j in 0 until w) {
                data[k++] = 0xFF.toByte()
                data[k++] = 0xFF.toByte()
                data[k++] = 0xFF.toByte()
                data[k++] = 0xFF.toByte()
            }
            if (i == h - 1) m = 30
            for (t in 0 until m) {
                for (j in 0 until w - 1) {
                    data[k++] = 0x80.toByte()
                    data[k++] = 0.toByte()
                    data[k++] = 0.toByte()
                    data[k++] = 0.toByte()
                }
                data[k++] = 0x80.toByte()
                data[k++] = 0.toByte()
                data[k++] = 0.toByte()
                data[k++] = 0x01.toByte()
            }
        }
        for (j in 0 until w) {
            data[k++] = 0xFF.toByte()
            data[k++] = 0xFF.toByte()
            data[k++] = 0xFF.toByte()
            data[k++] = 0xFF.toByte()
        }
        data[k++] = 0x0A
        return data
    }

    fun getZXingQRCode(data: String?, size: Int): ByteArray? {
        try {
            val hints = Hashtable<EncodeHintType, String?>()
            hints[EncodeHintType.CHARACTER_SET] = "utf-8"
            val bitMatrix = QRCodeWriter().encode(data, BarcodeFormat.QR_CODE, size, size, hints)
            //System.out.println("bitmatrix height:" + bitMatrix.getHeight() + " width:" + bitMatrix.getWidth());
            return getBytesFromBitMatrix(bitMatrix)
        } catch (e: WriterException) {
            // TODO Auto-generated catch block
            e.printStackTrace()
        }
        return null
    }

    fun getBytesFromBitMatrix(bits: BitMatrix?): ByteArray? {
        if (bits == null) return null
        val h = bits.height
        val w = (bits.width + 7) / 8
        val rv = ByteArray(h * w + 4)
        rv[0] = w.toByte() //xL
        rv[1] = (w shr 8).toByte() //xH
        rv[2] = h.toByte()
        rv[3] = (h shr 8).toByte()
        var k = 4
        for (i in 0 until h) {
            for (j in 0 until w) {
                for (n in 0..7) {
                    val b = getBitMatrixColor(bits, j * 8 + n, i)
                    rv[k] = ((rv[k]) + b).toByte()
                }
                k++
            }
        }
        return rv
    }

    private fun getBitMatrixColor(bits: BitMatrix, x: Int, y: Int): Byte {
        val width = bits.width
        val height = bits.height
        if (((x >= width) || y >= height || x < 0) || y < 0) return 0
        return if (bits.get(x, y)) {
            1
        } else {
            0
        }
    }

    fun getBytesFromBitMap(bitmap: Bitmap): ByteArray {
        val width = bitmap.width
        val height = bitmap.height
        val bw = (width - 1) / 8 + 1
        val rv = ByteArray(height * bw + 4)
        rv[0] = bw.toByte() //xL
        rv[1] = (bw shr 8).toByte() //xH
        rv[2] = height.toByte()
        rv[3] = (height shr 8).toByte()
        val pixels = IntArray(width * height)
        bitmap.getPixels(pixels, 0, width, 0, 0, width, height)
        for (i in 0 until height) {
            for (j in 0 until width) {
                val clr = pixels[width * i + j]
                val red = clr and 0x00ff0000 shr 16
                val green = clr and 0x0000ff00 shr 8
                val blue = clr and 0x000000ff
                val gray = RGB2Gray(red, green, blue)
                rv[(width * i + j) / 8 + 4] =
                    (rv[(width * i + j) / 8 + 4].toInt() or (gray.toInt() shl 7 - (width * i + j) % 8)).toByte()
            }
        }
        return rv
    }

    private fun RGB2Gray(r: Int, g: Int, b: Int): Byte {
        return if ((if (false) (0.29900 * r + 0.58700 * g + 0.11400 * b).toInt() > 200 else (0.29900 * r + 0.58700 * g + 0.11400 * b).toInt() < 200)) 1.toByte() else 0.toByte()
    }

    fun initBlackBlock(w: Int): ByteArray {
        val ww = (w + 7) / 8
        val n = (ww + 11) / 12
        val hh = n * 24
        val data = ByteArray(hh * ww + 5)
        data[0] = ww.toByte() //xL
        data[1] = (ww shr 8).toByte() //xH
        data[2] = hh.toByte()
        data[3] = (hh shr 8).toByte()
        var k = 4
        for (i in 0 until n) {
            for (j in 0..23) {
                for (m in 0 until ww) {
                    if (m / 12 == i) {
                        data[k++] = 0xFF.toByte()
                    } else {
                        data[k++] = 0
                    }
                }
            }
        }
        data[k++] = 0x0A
        return data
    }

    fun initBlackBlock(h: Int, w: Int): ByteArray {
        val hh = h
        val ww = (w - 1) / 8 + 1
        val data = ByteArray(hh * ww + 6)
        data[0] = ww.toByte() //xL
        data[1] = (ww shr 8).toByte() //xH
        data[2] = hh.toByte()
        data[3] = (hh shr 8).toByte()
        var k = 4
        for (i in 0 until hh) {
            for (j in 0 until ww) {
                data[k++] = 0xFF.toByte()
            }
        }
        data[k++] = 0x00
        data[k++] = 0x00
        return data
    }

