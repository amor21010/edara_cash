    fun getHexStringFromBytes(data: ByteArray?): String? {
        if (data == null || data.size <= 0) {
            return null
        }
        val hexString = "0123456789ABCDEF"
        val size = data.size * 2
        val sb = StringBuilder(size)
        for (i in data.indices) {
            sb.append(hexString[(data[i].toInt() and 0xF0) shr 4])
            sb.append(hexString[(data[i].toInt() and 0x0F) shr 0])
        }
        return sb.toString()
    }

    private fun charToByte(c: Char): Byte {
        return "0123456789ABCDEF".indexOf(c).toByte()
    }

    @SuppressLint("DefaultLocale")
    fun getBytesFromHexString(hexstring: String?): ByteArray? {
        var hexstring = hexstring
        if ((hexstring == null) || hexstring == "") {
            return null
        }
        hexstring = hexstring.replace(" ", "")
        hexstring = hexstring.uppercase(Locale.getDefault())
        val size = hexstring.length / 2
        val hexarray = hexstring.toCharArray()
        val rv = ByteArray(size)
        for (i in 0 until size) {
            val pos = i * 2
            rv[i] = (charToByte(hexarray[pos]).toInt() shl 4 or charToByte(
                hexarray[pos + 1]
            ).toInt()).toByte()
        }
        return rv
    }

    fun byteMerger(byte_1: ByteArray, byte_2: ByteArray): ByteArray {
        val byte_3 = ByteArray(byte_1.size + byte_2.size)
        System.arraycopy(byte_1, 0, byte_3, 0, byte_1.size)
        System.arraycopy(byte_2, 0, byte_3, byte_1.size, byte_2.size)
        return byte_3
    }

